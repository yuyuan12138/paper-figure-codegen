def validate_data(data):

        series_names, epochs, data, confidence_intervals = data
        if len(data) != len(series_names):
            raise ValueError(f"Expected {len(series_names)} series, got {len(data)}")
        for name in series_names:
            if name not in data:
                raise ValueError(f"Series {name} not found in data")
            if len(data[name]) != len(epochs):
                raise ValueError(f"Series {name}: expected {len(epochs)} values, got {len(data[name])}")


