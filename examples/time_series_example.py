def plot_figure(data):

        apply_paper_style()
        series_names, epochs, data, confidence_intervals = data

        fig, ax = plt.subplots(figsize=(7, 4.5))

        colors = [
            "#5B8DB8",
            "#6FA8A6",
        ]

        for i, name in enumerate(series_names):
            color = colors[i % len(colors)]
            values = data[name]
            ax.plot(epochs, values, label=name, color=color, linewidth=2.0)
            if name in confidence_intervals:
                lower, upper = confidence_intervals[name]
                ax.fill_between(epochs, lower, upper, color=color, alpha=0.2)

        ax.set_xlabel("Training Epoch")
        ax.set_ylabel("Accuracy")
        ax.set_title("Training Curve with 95% Confidence Interval")
        ax.legend(loc="lower right")
        ax.grid(linestyle="--", linewidth=0.6, alpha=0.35)
        ax.set_ylim(0.5, 1.0)

        fig.tight_layout(pad=1.2)
        return fig


