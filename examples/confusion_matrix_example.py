def validate_data(data):

        class_names, matrix = data
        if matrix.shape[0] != matrix.shape[1]:
            raise ValueError(f"Confusion matrix must be square, got {matrix.shape}")
        if len(class_names) != matrix.shape[0]:
            raise ValueError(f"Class names ({len(class_names)}) != matrix size ({matrix.shape[0]})")
        for i, row in enumerate(matrix):
            row_sum = row.sum()
            if not (0.95 <= row_sum <= 1.05):
                print(f"Warning: Row {i} ({class_names[i]}) sums to {row_sum:.3f}, expected ~1.0")


