def plot_figure(data):

        apply_paper_style()
        class_names, matrix = data

        fig, ax = plt.subplots(figsize=(6, 5.5))

        im = ax.imshow(matrix, cmap="Blues", aspect="auto", vmin=0, vmax=1)

        ax.set_xticks(np.arange(len(class_names)))
        ax.set_yticks(np.arange(len(class_names)))
        ax.set_xticklabels(class_names)
        ax.set_yticklabels(class_names)

        for i in range(len(class_names)):
            for j in range(len(class_names)):
                value = matrix[i, j]
                text_color = "white" if value < 0.6 else "black"
                if i == j:
                    font_weight = "bold"
                else:
                    font_weight = "normal"
                ax.annotate(f"{value:.0%}",
                           xy=(j, i), ha="center", va="center",
                           color=text_color, fontsize=10, fontweight=font_weight)

        ax.set_xlabel("Predicted Label")
        ax.set_ylabel("True Label")
        ax.set_title("Confusion Matrix (Normalized per True Class)")

        fig.tight_layout(pad=1.2)
        return fig


